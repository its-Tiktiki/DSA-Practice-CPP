#include <bits/stdc++.h>
using namespace std;
int main(){
    int a = 4, b = 5;
    cout << a << " & " << b << " = " << (a & b) << endl;
    cout << a << " | " << b << " = " << (a |  b) << endl;
    cout << 5 << " ^ " << 3 << " = " << (5 ^ 3) << endl;

    cout << 5 << " << 1 = " << (5 << 1) << endl;
    cout << 4 << " >> 1 = " << (4 >> 1) << endl;

    return 0;
}